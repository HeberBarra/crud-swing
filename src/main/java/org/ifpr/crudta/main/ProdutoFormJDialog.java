    private void salvarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarBTActionPerformed
        if (!isFormValid()) {
            JOptionPane.showMessageDialog(this, "Formulário inválido, por favor verifique os campos em vermelho", "ERRO DE VERIFICAÇÃO", JOptionPane.WARNING_MESSAGE);
            return;
        }

        formToObject();

        if (produto.getId() == 0) {
            produtoDAO.create(produto);
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso.", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        } else {
            produtoDAO.update(produto);
            JOptionPane.showMessageDialog(this, "Produto atualizado com sucesso", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
        }

        this.dispose();
    }//GEN-LAST:event_salvarBTActionPerformed

