    public void objectToForm() {
        nomeTF.setText(produto.getNome());
        marcaTF.setText(produto.getMarca());
        dataFabricacaoTF.setText(produto.getDataFabricacao().format(dateTimeFormatter));
        precoTF.setText(produto.getPreco().toString());
    }

    public void formToObject() {
        produto.setNome(nomeTF.getText());
        produto.setMarca(marcaTF.getText());
        produto.setDataFabricacao(LocalDate.parse(dataFabricacaoTF.getText(), dateTimeFormatter));
        produto.setPreco(new BigDecimal(precoTF.getText()));
    }

    private boolean isFormValid() {
        boolean isValid = true;

        if (nomeTF.getText().strip().isBlank()) {
            nomeTF.setBorder(bordaErro);
            isValid = false;
        } else {
            nomeTF.setBorder(bordaOriginal);
        }

        if (marcaTF.getText().strip().isBlank()) {
            marcaTF.setBorder(bordaErro);
            isValid = false;
        } else {
            marcaTF.setBorder(bordaOriginal);
        }

        if (dataFabricacaoTF.getText().strip().isBlank() || dataFabricacaoTF.getText().strip().length() != 10) {
            dataFabricacaoTF.setBorder(bordaErro);
            isValid = false;
        } else {
            dataFabricacaoTF.setBorder(bordaOriginal);
        }

        if (precoTF.getText().strip().isBlank()) {
            precoTF.setBorder(bordaErro);
            isValid = false;
        } else {
            precoTF.setBorder(bordaOriginal);
        }

        try {
            new BigDecimal(precoTF.getText());
            precoTF.setBorder(bordaOriginal);
        } catch (NumberFormatException e) {
            precoTF.setBorder(bordaErro);
            isValid = false;
        }


        return isValid;
    }

    public void limparCampos() {
        nomeTF.setText("");
        nomeTF.setBorder(bordaOriginal);
        marcaTF.setText("");
        marcaTF.setBorder(bordaOriginal);
        dataFabricacaoTF.setText("");
        dataFabricacaoTF.setValue(null);
        dataFabricacaoTF.setBorder(bordaOriginal);
        precoTF.setText("");
        precoTF.setValue(null);
        precoTF.setBorder(bordaOriginal);
    }

